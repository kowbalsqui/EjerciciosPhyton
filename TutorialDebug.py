def suma (a,b):
    suma = a + b
    return suma

def resta (a,b):
    resta = a - b
    return resta

def multiplicacion (a,b):
    multiplicacion = a * b
    return multiplicacion

def division (a,b):
    division = a / b
    return division

numero1 = 10
numero2 = 5

print(str(suma(numero1, numero2)))
print(str(resta(numero1, numero2)))
print(str(multiplicacion(numero1, numero2)))
print(str(division(numero1, numero2)))